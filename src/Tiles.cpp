#include "Tiles.h"

Tiles::Tiles()
{
}

//---------------------------------------------//

void Tiles::addTile(const CompNode& tile)
{
	m_tilesList.push_back(tile);
}

//---------------------------------------------//

void Tiles::clearTile(const int& cell)
{
	m_tilesList.erase(m_tilesList.begin() + cell);
}

